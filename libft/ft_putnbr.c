#include "libft.h"

void	ft_putnbr(int n)
{
	char	c;

	c = n % 10 + 48;
	if (n < 0)
	{
		ft_putchar('-');
		c = -(n % 10) + 48;
	}
	n /= 10;
	if (n < 0)
		n = -n;
	if (n > 0)
		ft_putnbr(n);
	ft_putchar(c);
}
