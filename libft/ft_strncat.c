#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	j;
	size_t	k;

	i = 0;
	j = 0;
	k = 0;
	while (s1[i])
		i++;
	while (s2[j] && k++ < n)
		s1[i++] = s2[j++];
	s1[i] = '\0';
	return (s1);
}
