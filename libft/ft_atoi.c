int		ft_atoi(char const *s)
{
	int				i;
	int				n;
	int				nb;
	unsigned long	err;

	i = 0;
	n = 1;
	nb = 0;
	err = 0;
	while (s[i] == '\t' || s[i] == '\r' || s[i] == '\n' || s[i] == '\v' ||
			s[i] == '\f' || s[i] == ' ')
		i++;
	if (s[i] == '-')
		n = -1;
	if (s[i] == '-' || s[i] == '+')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		nb = nb * 10 + s[i] - 48;
		err = err * 10 + s[i] - 48;
		i++;
		if (err > 9223372036854775807)
			return (n == 1 ? -1 : 0);
	}
	return (nb * n);
}
