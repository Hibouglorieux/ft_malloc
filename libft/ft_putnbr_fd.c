#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	c = n % 10 + 48;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		c = -(n % 10) + 48;
	}
	n /= 10;
	if (n < 0)
		n = -n;
	if (n > 0)
		ft_putnbr_fd(n, fd);
	ft_putchar_fd(c, fd);
}
